package com.mmorpg.mir.model.beauty.resource;

public class Range {

	private int min;

	private int max;

	private int value;

	public int getMin() {
		return min;
	}

	public int getMax() {
		return max;
	}

	public int getValue() {
		return value;
	}

	public void setMin(int min) {
		this.min = min;
	}

	public void setMax(int max) {
		this.max = max;
	}

	public void setValue(int value) {
		this.value = value;
	}

}
